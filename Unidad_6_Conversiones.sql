--B) NUMERICOS
DECLARE @NUM4 DECIMAL(10,2) = 31020.56
SELECT
ORIGINAL = @NUM4,
ENTERO = CAST(@NUM4 AS INT),
[SMALLINT] = CAST(@NUM4 AS SMALLINT),
[FLOAT] = CAST(@NUM4 AS FLOAT),
[TEXTO] = CAST(@NUM4 AS VARCHAR(10))