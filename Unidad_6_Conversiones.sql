--CONVERSIONES IMPLICITAS
--UN INT A UN VARCHAR
DECLARE @NUM1 INT = 1521
DECLARE @texto VARCHAR(50) = @NUM1
SELECT @NUM1, @texto

--UN BIT A UN SMALLINT
DECLARE @FLAG BIT = 1
DECLARE @NUM SMALLINT = @FLAG
SELECT @FLAG, @NUM

--UN INT A UN DECIMAL
DECLARE @NUM2 INT = 12352
DECLARE @NUM3 DECIMAL(9,2) = @NUM2
SELECT @NUM2, @NUM3

